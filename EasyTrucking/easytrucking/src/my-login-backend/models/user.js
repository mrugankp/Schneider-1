const { DataTypes } = require('sequelize');
const sequelize = require('../sequelize'); // Make sure the path is correct

const User = sequelize.define('User', {
  email: {
    type: DataTypes.STRING,
    allowNull: false,
    unique: true
  },
  password: {
    type: DataTypes.STRING,
    allowNull: false
  }
});

module.exports = User;